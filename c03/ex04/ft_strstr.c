char	*ft_strstr(char *str, char *to_find)
{
	int		i;
	int		j;

	i = 0;
	if (*to_find == '\0')
		return (str);
	while (str[i] != '\0')
	{
		j = 0;
		while (str[i + j] == to_find[j])
		{
			j++;
			if (to_find[j] == '\0')
				return (&str[i]);
		}
	i++;
	}
	return (0);
}

int main()
{
    char dest[] = "hello world yolo hehe";
    char i[] = "world";
    printf("%s", ft_strstr(dest, i));
    //printf("%s", strstr(dest, i));
}

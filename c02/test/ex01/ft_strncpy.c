#include <unistd.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (src[i] != '\0' && i <= (n - 1))
	{
		dest[i] = src[i];
		i++;
	}
	dest = '\0';
	return (dest);
}
